function playerPosToMapPos(x, y) {
    var rect = clientStorage.bandZonesRect;
    if (x < rect.x1 || x > rect.x2) return null;
    if (y < rect.y1 || y > rect.y2) return null;

    x -= rect.x1;
    y -= rect.y1;

    var width = $("#bandZones").width();
    var height = $("#bandZones").height();

    x = x / (rect.x2 - rect.x1) * width;
    y = height - y / (rect.y2 - rect.y1) * height;

    return {
        x: x,
        y: y
    };
}

function mapPosToPlayerPos(x, y) {
    var rect = {
        x1: 0,
        y1: 0,
        x2: $("#bandZones").width(),
        y2: $("#bandZones").height(),
    };
    if (x < rect.x1 || x > rect.x2) return null;
    if (y < rect.y1 || y > rect.y2) return null;
    var bandZonesRect = clientStorage.bandZonesRect;

    var width = $("#bandZones").width();
    var height = $("#bandZones").height();

    x = x / width * (bandZonesRect.x2 - bandZonesRect.x1) + bandZonesRect.x1;
    y = -(y - height) / height * (bandZonesRect.y2 - bandZonesRect.y1) + bandZonesRect.y1;

    return {
        x: x,
        y: y
    };
}

// принадлежит ли позиция зоне
function inZone(pos, zoneId) {
    var polygon = bandZonesAPI.colliders[zoneId - 1];

    var parity = 0;
    for (var i = 0; i < polygon.length - 1; i++) {
        var v = {
            x1: polygon[i].x,
            y1: polygon[i].y,
            x2: polygon[i + 1].x,
            y2: polygon[i + 1].y
        }
        switch (edgeType(v, pos)) {
            case 0:
                return 2;
                break;
            case 1:
                parity = 1 - parity;
                break;
        }
    }
    var v = {
        x1: polygon[polygon.length - 1].x,
        y1: polygon[polygon.length - 1].y,
        x2: polygon[0].x,
        y2: polygon[0].y
    }
    switch (edgeType(v, pos)) {
        case 0:
            return 2;
            break;
        case 1:
            parity = 1 - parity;
            break;
    }
    return parity;
}

// ребро касается, пересекается или пох
function edgeType(vector, a) {
    switch (classify(vector, a.x, a.y)) {
        case 1:
            return ((vector.y1 < a.y) && (a.y <= vector.y2)) ? 1 : 2;
            break;
        case -1:
            return ((vector.y2 < a.y) && (a.y <= vector.y1)) ? 1 : 2;
            break;
        case 0:
            return 0;
            break;
    }
}

// слева от вектора, справа от вектора, или принадлежит вектору
function classify(vector, x1, y1) {
    var pr = (vector.x2 - vector.x1) * (y1 - vector.y1) - (vector.y2 - vector.y1) * (x1 - vector.x1);
    if (pr > 0)
        return 1;
    if (pr < 0)
        return -1;
    return 0;
}

